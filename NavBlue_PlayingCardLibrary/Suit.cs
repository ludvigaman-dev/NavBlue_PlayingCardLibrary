namespace NavBlue_PlayingCardLibrary
{
    /// <summary>
    /// Suit enum has the possibility to give each suit a custom value in case
    /// of the deck / game values the cards differently than the standard card game.
    /// </summary>
    public enum Suit
    {

        Clubs = 1,
        Diamonds = 2,
        Hearts = 3,
        Spades = 4
    }
}
